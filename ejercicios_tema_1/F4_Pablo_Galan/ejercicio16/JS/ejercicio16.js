let numero = prompt("Introduce un numero");

console.log("El primero se sumará despues de la sentencia");
console.log("Este es un ejemplo de a++ " + numero++);
console.log("El segundo se ejecutará al instante, siendo 2 más que el primero al haberse hecho la suma anterior");
console.log("Este es un ejemplo de ++a " + ++numero);
console.log("El tercero se restará despues de la sentencia");
console.log("Este es un ejemplo de a-- " + numero--);
console.log("El cuarto se ejecutará al instante, siendo 2 menos que el tercero al haberse hecho la resta anterior");
console.log("Este es un ejemplo de --a " + --numero);